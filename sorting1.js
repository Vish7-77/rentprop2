async function show_all_data() {
  const property_location = await sessionStorage.getItem("location");
  const property_type = await sessionStorage.getItem("type");
  console.log(String(property_location), property_type);

  if (property_location && property_type) {
    const filterd_data = newdata.filter(
      (e) => e.area == property_location && e.BHK == property_type
    );

    if (filterd_data.length > 0) {
      const first_data = filterd_data[0];

      document.getElementById("title1").innerText = first_data.title;
      document.getElementById("beds").innerText = `${first_data.BHK} BHK`;
      document.getElementById(
        "sqrt"
      ).innerText = `${first_data.Sqrt} Sq.feet area`;
      document.getElementById("bath").innerText = `${first_data.bath} bath`;
      document.getElementById("zip").innerText = ` ${first_data.price}/ month`;

      for (let i = 0; i < first_data.image.length; i++) {
        const red = document.getElementById("red");
        const again_div = document.createElement("div");
        const image = document.createElement("img");
        image.classList.add("h-auto", "max-w-full", "rounded-lg");
        image.src = first_data.image[i];
        again_div.append(image);
        red.append(again_div);
      }
    } else {
      const red = document.getElementById("red");
      red.innerHTML = `<span className="font-bold flex m-5"> No data Found </span> `;
    }
  } else {
  }
}

